package ex_08_Switch;

public class Lab072_Interview {
    public static void main(String[] args) {
        char code  = 'C';
        switch (code)
        {
            default :
                System.out.println("Hello");
                break;
            case 'A' :
                System.out.println("65");
                break;
            case 'B' :
                System.out.println("66");
        }
    }
}
