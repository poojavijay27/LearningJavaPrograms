package ex_09_For_Loop;

public class Lab077_For_If_LOOP {
    public static void main(String[] args) {

        for (int ash = 0 ; ash < 18 ; ash++)
        {
            if(ash > 15)
            {
                System.out.println("Gift from mom, Iphone");
            }
            else
            {
                System.out.println("No Gift");
            }
        }

    }
}
