package ex_03_Operators;

public class Lab021_Logical_Operators {
    public static void main(String[] args) {

        boolean a = true;
        System.out.println(!a);

        boolean b = false;
        System.out.println(!!b);

        boolean c = true || false;
        System.out.println(c);

        boolean d = true & false;
        System.out.println(c);

    }
}
